/**
 * Get category image URL by category name
 * Falls back to default image if not found
 */
export function getCategoryImage(categoryName) {
  if (!categoryName) return null;
  
  const nameLower = categoryName.toLowerCase().trim();
  
  // Try exact match first
  if (categoryImageMap[nameLower]) {
    return categoryImageMap[nameLower];
  }
  
  // Try partial match
  for (const [key, value] of Object.entries(categoryImageMap)) {
    if (nameLower.includes(key) || key.includes(nameLower)) {
      return value;
    }
  }
  
  // Default fallback
  return "https://images.unsplash.com/photo-1512621776951-a57141f2eefd?auto=format&fit=crop&q=80&w=300&h=300";
}
